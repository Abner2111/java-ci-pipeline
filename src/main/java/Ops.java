public class Ops{
    public  float add(float num1, float num2){
        return num1 + num2;
    }

    public  float substract(float num1, float num2){
        return num1 - num2;
    }

    public float divide(float num1, float num2){
        return num1/num2;
    }

    public float product(float num1, float num2){
        return num1*num2;
    }
}
